<p/><b>FAQ</b>

<p/><b>1- Can I make my own channel on the server?</b>

<p/>Sure! Your channel can be created by using <tt>/join #name</tt>, where <tt>#name</tt> is the name of your channel. Your channel name must begin with a pound sign (<tt>#</tt>).

<p/>Your channel can be registered by following the instructions after you type:

<p/><tt>/msg chanserv help register</tt>

<p/><b>2- How can I stop people from using my nick when I'm not online?</b>

<p/>Once you're chatting with a nickname you want to keep, follow the instructions after you type:

<p/><tt>/msg nickserv help register</tt>
