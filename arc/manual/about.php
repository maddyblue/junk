<p/><b>What is Crescent Island?</b>

<p/>CI is a web based game (like Archmage and Planetarion) with many influences from Final Fantasy Tactics. We are currently still working on the fourth version of CI.

<p/><b>History of CI</b>

<p/>CI started as an idea by Axloschak of the <a href="http://carchave.net">Character Archive Network</a>. It was originally email based (CI1), and was thus slow and sort of crude. Axloschak solicited help and Dolmant volunteered to develop some scripts for it (but it was still going to remain email based, with no database backend). Dolmant decided that this thing was cool enough to warrant more interaction, so he started to work on a database-ized version of the game. Axloschak quickly joined forces, though. This version (CI2) was originally in a playable state <a href="http://web.archive.org/web/20010721231431/http://rpgamers.net/">July 17, 2001</a> (second news post).

<p/>After some time, Khraythia (a real life friend of dolmant) joined the development team and started making improvements (CI3). These were completed around Christmas of 2001 or 2002, I (dolmant) forget which. After many more months of gaming, CI3 was becoming annoying to upkeep, since things would often break. CI3 was turned off and CI4 was started. We have been in development of CI4 for a real long time.
